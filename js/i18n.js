// ── LANGUAGE ENGINE ──
let currentLang = 'ar';

function applyLang(lang) {
  currentLang = lang;
  const t = translations[lang];
  const isAr = lang === 'ar';

  // Direction + lang attribute
  document.documentElement.lang = lang;
  document.documentElement.dir  = isAr ? 'rtl' : 'ltr';
  document.body.classList.toggle('rtl', isAr);
  document.body.classList.toggle('ltr', !isAr);

  // Apply all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // Placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.dataset.i18nPh;
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // Counter suffixes (update data-suffix so counter animation picks it up)
  document.querySelectorAll('[data-i18n-suffix]').forEach(el => {
    const key = el.dataset.i18nSuffix;
    if (t[key] !== undefined) el.dataset.suffix = t[key];
  });

  // Save preference
  localStorage.setItem('wwg-lang', lang);

  // Update lang toggle button text
  const btn = document.getElementById('lang-toggle');
  if (btn) btn.textContent = t['lang.switch'];
}

function initLang() {
  const saved = localStorage.getItem('wwg-lang') || 'ar';
  applyLang(saved);

  const btn = document.getElementById('lang-toggle');
  if (btn) {
    btn.addEventListener('click', () => {
      applyLang(currentLang === 'ar' ? 'en' : 'ar');
    });
  }
}
